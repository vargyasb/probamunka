package com.gbsolutions.probamunka.dto;

public class ClientDTO {
	
	private String name;

    private String email;
   
	public ClientDTO() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
    
    
}
